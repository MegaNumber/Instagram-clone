// مسیر فایل: /routes/index.js
// توضیح: فایل مرکزی تعریف تمام مسیرهای API. در این فایل هر گروه از امکانات
// (مانند احراز هویت، کاربران، پست‌ها، نظرات، استوری‌ها و ...) به زیرروتر
// مخصوص خود هدایت می‌شود. این ساختار، نگهداری و گسترش پروژه را ساده می‌کند.

// ============================================================
// بخش ۱: ایجاد نمونهٔ اصلی Router
// ============================================================
const router = require('express').Router();

// ============================================================
// بخش ۲: بارگذاری زیرمسیرها (Sub-Routes)
// ============================================================

// ۲.۱. احراز هویت (ورود، ثبت‌نام، تغییر رمز، تأیید ایمیل)
router.use('/auth', require('./auth'));

// ۲.۲. مدیریت کاربران (پروفایل، دنبال‌کردن، آواتار و ...)
router.use('/users', require('./user'));

// ۲.۳. مدیریت پست‌ها (ایجاد، حذف، رأی‌دهی، فید اصلی)
router.use('/posts', require('./post'));

// ۲.۴. مدیریت نظرات (کامنت‌ها و پاسخ‌ها)
router.use('/comments', require('./comment'));

// ۲.۵. مدیریت نوتیفیکیشن‌ها (اعلان‌های بلادرنگ)
router.use('/notifications', require('./notification'));

// ۲.۶. استوری‌ها (استوری‌های ۲۴ ساعته)
router.use('/stories', require('./story'));

// ۲.۷. پیام‌رسانی مستقیم (Direct Messages)
router.use('/messages', require('./message'));

// ۲.۸. ریلز (Reels - ویدئوهای کوتاه)
router.use('/reels', require('./reel'));

// ۲.۹. فید هوشمند (الگوریتم رتبه‌بندی پیشرفته)
router.use('/feed', require('./feed'));

// ۲.۱۰. گزارش‌دهی (گزارش پست‌ها، کاربران و ...)
router.use('/reports', require('./report'));

// ============================================================
// بخش ۳: صادرات router برای استفاده در index.js
// ============================================================
module.exports = router;
